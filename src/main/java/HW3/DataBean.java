package HW3;

public class DataBean
{
  
  private double mean;
  private double standardDeviation;
  
  public DataBean() {}
  
  public void setMean(double mean) {this.mean = mean;}
  
  public void setStDeviation(double standardDeviation) {this.standardDeviation = standardDeviation;}
  
  public double getMean() {return mean;}
  
  public double getStDeviation() {return standardDeviation;}
  
}